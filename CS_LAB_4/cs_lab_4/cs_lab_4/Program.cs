        // Generate 48 random bits
        Random random = new();
        byte[] randomBytes = new byte[6];
        random.NextBytes(randomBytes);

        string bitsAsStr = new("");
        foreach (byte oneByte in randomBytes)
        {
            bitsAsStr += Convert.ToString(oneByte, 2).PadLeft(8, '0');
        }

        Console.WriteLine("48 biti aleatori: " + bitsAsStr);

        // Accept the group number from the user
        int groupNumber = 0;
        while (true)
        {
            Console.Write("Enter the group number (1-8): ");
            if (int.TryParse(Console.ReadLine(), out groupNumber) && groupNumber >= 1 && groupNumber <= 8)
                break;
            else
                Console.WriteLine("Invalid input. Please enter a number between 1 and 8.");
        }

        int bitsPerGroup = 6;
        int startingBitPosition = (groupNumber - 1) * bitsPerGroup;
        var groupOfRequestedBits = bitsAsStr.Substring(startingBitPosition, bitsPerGroup);

        var rowAsStrOfBits = new string(new[] { groupOfRequestedBits[0], groupOfRequestedBits[5] }); // the first and the last bits
        var colAdStrOfBits = groupOfRequestedBits.Substring(1, 4); // the remaining bits, between 1 and 6

        var resultRow = Convert.ToInt32(rowAsStrOfBits, 2);
        var resultCol = Convert.ToInt32(colAdStrOfBits, 2);

        var resultFromSBox = sBoxes[groupNumber - 1, resultRow, resultCol];
        var resultFromSBoxAsStr = Convert.ToString(resultFromSBox, 2).PadLeft(4, '0');

        Console.WriteLine($"Extracted 6 bits from group {groupNumber}: {groupOfRequestedBits}");
        Console.WriteLine($"The result was taken from S-Box {groupNumber}, row {resultRow+1}, col {resultCol+1}, and is: {resultFromSBox} ({resultFromSBoxAsStr})");
    }
